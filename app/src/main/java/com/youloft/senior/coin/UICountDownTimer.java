package com.youloft.senior.coin;

import android.os.Handler;

/**
 * Schedule a countdown until a time in the future, with
 * regular notifications on intervals along the way.
 * <p>
 * Example of showing a 30 second countdown in a text field:
 *
 * <pre class="prettyprint">
 * new UICountDownTimer(30000, 1000) {
 *
 *     public void onTick(long millisUntilFinished) {
 *         mTextField.setText("seconds remaining: " + millisUntilFinished / 1000);
 *     }
 *
 *     public void onFinish() {
 *         mTextField.setText("done!");
 *     }
 *  }.start();
 * </pre>
 * <p>
 * The calls to {@link #onTick(long)} are synchronized to this object so that
 * one call to {@link #onTick(long)} won't ever occur before the previous
 * callback is complete.  This is only relevant when the implementation of
 * {@link #onTick(long)} takes an amount of time to execute that is significant
 * compared to the countdown interval.
 */
public abstract class UICountDownTimer {

    /**
     * Millis since epoch when alarm should stop.
     */
    private final long mMillisInFuture;

    /**
     * The interval in millis that the user receives callbacks
     */
    private final long mCountdownInterval;

    private long mStopTimeInFuture;

    /**
     * boolean representing if the timer was cancelled
     */
    private boolean mCancelled = false;

    /**
     * @param millisInFuture    The number of millis in the future from the call
     *                          to {@link #start()} until the countdown is done and {@link #onFinish()}
     *                          is called.
     * @param countDownInterval The interval along the way to receive
     *                          {@link #onTick(long)} callbacks.
     */
    public UICountDownTimer(long millisInFuture, long countDownInterval) {
        mMillisInFuture = millisInFuture;
        mCountdownInterval = countDownInterval;
    }

    /**
     * Cancel the countdown.
     */
    public synchronized final void cancel() {
        mCancelled = true;
        sHandler.removeCallbacksAndMessages(mLoopRunnable);
    }

    /**
     * Start the countdown.
     */
    public synchronized final UICountDownTimer start() {
        mCancelled = false;
        if (mMillisInFuture <= 0) {
            onFinish();
            return this;
        }
        mStopTimeInFuture = mMillisInFuture;
        sHandler.removeCallbacksAndMessages(mLoopRunnable);
        sHandler.postDelayed(mLoopRunnable, mCountdownInterval);
        onTick(mStopTimeInFuture);
        return this;
    }


    /**
     * Callback fired on regular interval.
     *
     * @param millisUntilFinished The amount of time until finished.
     */
    public abstract void onTick(long millisUntilFinished);

    /**
     * Callback fired when the time is up.
     */
    public abstract void onFinish();


    private final Runnable mLoopRunnable = new Runnable() {
        @Override
        public void run() {
            synchronized (UICountDownTimer.this) {
                if (mCancelled) {
                    return;
                }

                mStopTimeInFuture -= mCountdownInterval;

                if (mStopTimeInFuture <= 0) {
                    onFinish();
                } else {
                    onTick(mStopTimeInFuture);
                    sHandler.removeCallbacksAndMessages(this);
                    sHandler.postDelayed(this, mCountdownInterval);
                }
            }
        }
    };

    private static Handler sHandler = new Handler();
}
